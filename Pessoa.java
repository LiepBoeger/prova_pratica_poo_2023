class Pessoa {
    String nome;
    int idade;
    char sexo;
    boolean condutorEmbriagado;

    Pessoa(String nome, int idade, char sexo, boolean condutorEmbriagado) {
        this.nome = nome;
        this.idade = idade;
        this.sexo = sexo;
        this.condutorEmbriagado = condutorEmbriagado;
    }
}